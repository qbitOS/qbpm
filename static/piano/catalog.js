export function albumFor(id) {
  return ALBUMS.find((a) => a.id === id);
}

export function songsForAlbum(albumId) {
  return CATALOG.filter((s) => s.albumId === albumId);
}

export function readySongs() {
  return CATALOG.filter((s) => s.status === 'ready' && s.notes?.length);
}
